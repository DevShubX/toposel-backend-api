const { Schema, model, models } = require("mongoose");



const UserSchema = new Schema({
    username: {
        type: String,
        required: true,
        unique: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    fullName: {
        type: String,
        required: true
    },
    gender: {
        type: String,
        enum: ['Male', 'Female', 'Other'],
        required: true
    },
    dob: {
        type: Date,
        required: true
    },
    country: {
        type: String,
        required: true
    },
},
    {
        timestamps: true
    });


module.exports = models.User || model("User", UserSchema);